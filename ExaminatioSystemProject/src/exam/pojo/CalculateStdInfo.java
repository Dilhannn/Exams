package exam.pojo;

public class CalculateStdInfo {

	private String studentName;
	private String schoolNumber;
	private String visaResult;
	private String visaPercent;
	private String finalResult;
	private String finalPrecent;
	private String totalResult;
	private String degree;
	
	public String getStudentName() {
		return studentName;
	}
	
	public void setStudentName(String studentName) {
		this.studentName=studentName;
	}
	
	public String getSchoolName() {
		return schoolNumber;
	}
	
	public void setSchoolName(String schoolName) {
		this.schoolNumber=schoolNumber;
	}
	
	public String getVisaResult() {
		return visaResult;
	}
	
	public void setVisaResult(String visaResult) {
		this.visaResult=visaResult;
	}
	
	public String getVisaPercent() {
		return visaPercent;
	}
	
	public void setVisaPercent(String visaPercent) {
		this.visaPercent=visaPercent;
	}
	
	public String getFinalResult() {
		return finalResult;
	}
	
	public void setFinalResult(String finalResult){
		this.finalResult=finalResult;
		}
	
	public String getFinalPercent() {
		return finalPrecent;
	}
	
	public void setFinalPercent(String finalPercent) {
		this.finalPrecent=finalPercent;
	}
	
	public String getTotalResult() {
		return totalResult;
	}
	
	public void setTotalResult(String totalResult) {
		this.totalResult=totalResult;
	}
	
	public String getDegree() {
		return degree;
	}
	
	public void getDegree(String degree) {
		this.degree=degree;
	}
	
}


